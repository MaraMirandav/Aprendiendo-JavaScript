//OPERADORES DE ASIGNACIÃ“N:

let miNumero = 10
console.log(miNumero); //10

miNumero = 20;
console.log(miNumero); //cambia a 20

//1) Operador +=
miNumero += 5;
console.log(miNumero); //25

//2) Operador -=
miNumero -= 3;
console.log(miNumero) //22

//3) Operador *=
miNumero *= 2;
console.log(miNumero) //44

//4) Operador /=
miNumero /= 4;
console.log(miNumero) //11

//5) Operador %=
miNumero %= 2;
console.log(miNumero) //1

//6) Exponente **=
miNumero = 8;
miNumero **= 2;
console.log(miNumero) //64