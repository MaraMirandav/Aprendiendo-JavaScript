//Tipos de datos:

let miEntero = 10;
console.log(miEntero); //10
console.log(typeof miEntero); //Number

let miFlotante = 7.8;
console.log(miFlotante); //7.8
console.log(typeof miFlotante); //Number

let miCadena = "Hola";
console.log(miCadena); //Hola
console.log(typeof miCadena); //String

let miBoolean = true;
console.log(miBoolean); //true
console.log(typeof miBoolean); //Boolean

let miNull = null;
console.log(miNull); //Null
console.log(typeof miNull); //object

let miUndifined = undefined;
console.log(miUndifined); //undefined
console.log(typeof miUndifined); //undefined