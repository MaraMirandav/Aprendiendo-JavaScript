//1) CONVERTIR DE CADENA A NUMERO:
let a = '10', b = '20';
//Si son cadenas se concatenan
console.log(a + b); //1020

//Convertir a numeros los valores para que se sumen:
let suma = parseInt(a) + parseInt(b);
console.log(suma); //30

//2) CONVERTIR DE NUMERO A CADENA:
let c = 20, d = 10;
//si son valores numéricos, se suman
console.log(c + d);

//Convertir numero a cadena:
let concatenación = c.toString() + d.toString();
console.log(concatenación); //2010